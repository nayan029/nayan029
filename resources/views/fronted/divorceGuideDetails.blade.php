						<h5 class="sa-color2 mb-4 mt-5"><b>What isMutual Divorce?</b></h5>
						Divorce is the act of ending the institution of marriage which existed between two parties. All mutual obligations cease between the parties to the marriage and putting an end to all ties they are to live separately after the divorce.
						<br/><br/>
						The laws dealing with divorce are a set of personal laws like the Hindu Marriage Act,1955 for Hindus and includes Sikhs, Jains and Buddhists. Whereas the Christians are governed by the Indian Divorce Act and Muslims are governed by Personallaws of Divorce and also the Dissolution of Marriage Act,1939 and the Muslim Women (Protection of Rights on Divorce) Act,1986. The same way Parsis are governed by Parsi Marriage & Divorce Act,1936. Additionally, other marriages are governed by the Special Marriage Act,1954.
						<br/><br/>
						Under the Hindu Marriage Act,1955 the concept of divorce by mutual consent is expounded under Section 13-B. However, 13B (1) states that there are three requirements to be fulfilled if the parties are filing for a divorce via mutual consent before the court. They are as follows:
<br/><br/>
The parties have been residing separately for a span of 365 days or one year,
<br/><br/>
They have not been capable of living together,
<br/><br/>
The parties have mutually agreed that the marriage should be dissolved.
<br/><br/>
It also states that at the motion made by means of each the events not earlier than 6 months after the date of presentation of the stated petition and no longer later than 18 months of the stated date, the court on being satisfied after hearing both the parties and after making such an inquiry as it thinks fit, pass a decree of divorce which dissolves the marriage via mutual consent.
<br/><br/>
The period of 6 to 18 months is given as an opportunity for the parties to reflect on their decision.
						
						<h5 class="sa-color2 mb-4 mt-5"><b>Provisions governing law of maintenance are:</b></h5>
						Under Section- 24 of the Hindu Marriage Act either of the spouses is entitled to get paid for the expenses of the legal proceedings under the Act.<br/>
						Under Section- 25 of the Act, the court can direct the respondent to pay maintenance in the form of monthly payment or entire sum or even a periodical sum for a time not exceeding the life of the applicant.
						<br/><br/>
						The other important provisions relating to maintenance are Sections- 18 and 19 of the Hindu Adoption and Maintenance Act which was amended in 2015 and Section- 125 of the Code of Criminal Procedure.

						<h5 class="sa-color2 mb-4 mt-5"><b>“Irretrievable Breakdown Theory” of Marriage</b></h5>
						There have been many instances where couples are cohabiting but their marriage is equivalent to a separation and there is no classified law for this.<br/><br/>

						In regard to this, a Report by the Law Commission as well as the Supreme Court was directed to the Government in 2009, in order to add irretrievable breakdown of marriage as a valid ground for divorce under Section- 13 of the Hindu Marriage Act, stating wisdom lies in accepting the pragmatic reality of life and thus, must take a decision which would ultimately be conducive for the common betterment of both the spouses .<br/><br/>

						These recommendations had resulted from the case Naveen Kohli v. Neetu Kohli after the couple proved that there was no reasonable chance of getting back together after the marriage broke down.
